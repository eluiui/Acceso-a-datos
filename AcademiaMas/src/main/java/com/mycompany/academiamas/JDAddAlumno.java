    private void llenarCombo() {
        Iterator<Modulo> iter = padre.getListaModulos().iterator();
        while (iter.hasNext()) {
            Modulo modulo = iter.next();
            comboModel.addElement(modulo.getNombre());
        }
        comboModulos.setModel(comboModel);
    }

