    private void comboModulosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboModulosActionPerformed
        if (evt.getActionCommand().equals("comboBoxChanged")) {
            String nombreModulo = comboModulos.getSelectedItem().toString();
            listaModulos.add(padre.getFromModulosByNombre(nombreModulo));
            llenarListaModulos();
        }
    }//GEN-LAST:event_comboModulosActionPerformed

    private void guardarAlumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarAlumnoActionPerformed
        Alumno alumno = new Alumno(textNombreAlumno.getText(), textLocalidadAlumno.getText(), listaModulos);
        if (comprobar(alumno)) {
            padre.addAlumnoLista(alumno);
            this.dispose();
        } else{
            JDialog errorDialog = new JDialog();
            errorDialog.setTitle("Error al guardar");
            errorDialog.setSize(300, 100);
            errorDialog.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
            errorDialog.setModalityType(Dialog.ModalityType.APPLICATION_MODAL);

            JLabel errorMessage = new JLabel("Valores incorrectos");
            errorDialog.add(errorMessage);

            errorDialog.setLocationRelativeTo(null);
            errorDialog.setVisible(true);
        }
    }//GEN-LAST:event_guardarAlumnoActionPerformed

    private void botonQuitarModuloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonQuitarModuloActionPerformed
        int index = listaModulosAlumno.getSelectedIndex();
        String nombreModulo = listModel.getElementAt(index);
        listModel.remove(index);
        listaModulos.remove(padre.getFromModulosByNombre(nombreModulo));
        llenarListaModulos();
    }//GEN-LAST:event_botonQuitarModuloActionPerformed

    private boolean comprobar(Alumno a) {
        int horas = 0;
        Iterator<Modulo> iter = listaModulos.iterator();
        while (iter.hasNext()) {
            Modulo modulo = iter.next();
            horas += modulo.getHoras();
        }
        if (a.getNombre() == "" || a.getLocalidad() == "" || horas > 30) {
            return false;
        }
        return true;
    }

    private void llenarListaModulos() {
        listModel.clear();
        Iterator<Modulo> iter = listaModulos.iterator();
        while (iter.hasNext()) {
            Modulo modulo = iter.next();
            listModel.addElement(modulo.getNombre());
        }
        listaModulosAlumno.setModel(listModel);
    }

